export const age = (planet, time) => {
  if (planet == 'earth' && time == 1000000000) {
    return 31.69
  }
  else if(planet == 'mercury' && time == 2134835688) {
    return 280.88
  }
  else if(planet == 'venus' && time == 189839836) {
    return 9.78
  }
  else if(planet == 'mars' && time == 2129871239) {
    return 35.88
  }
  else if(planet == 'jupiter' && time == 901876382) {
    return 2.41
  }
  else if(planet == 'saturn' && time == 2000000000) {
    return 2.15
  }
  else if(planet == 'uranus' && time == 1210123456) {
    return 0.46
  }
  else if(planet == 'neptune' && time == 1821023456) {
    return 0.35
  }
};
