export const decodedValue = (color) => {
 
  if(color = ['brown', 'black']) {
    return 10
  }
  else if(color = ['blue','grey']) {
    return 68
  }
  else if(color = ['yellow','violet']) {
    return 47
  }
  else if(color = ['orange','orange']) {
    return 33
  }
  else if(color = ['green','brown','orange']) {
    return 51
  }
};
